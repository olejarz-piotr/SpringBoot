package pl.olejarz.SpringDataTests;

import javax.persistence.*;

@Entity
@Table(name = "MyCar")
public class Car {

    @Id
    private int id;

    private String mark;

    @Transient //doesn't show in db
    private String model;

    @Enumerated(EnumType.STRING) //saving name of color not ID of Color enum
    private Color color;

    public Car(int id, String mark, String model, Color color) {
        this.id = id;
        this.mark = mark;
        this.model = model;
        this.color = color;
    }

    public Car() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMark() {
        return mark;
    }

    public void setMark(String mark) {
        this.mark = mark;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public Color getColor() {
        return color;
    }

    public void setColor(Color color) {
        this.color = color;
    }

    @Override
    public String toString() {
        return "Car{" +
                "id=" + id +
                ", mark='" + mark + '\'' +
                ", model='" + model + '\'' +
                ", color=" + color +
                '}';
    }
}
